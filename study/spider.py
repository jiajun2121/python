# 随机生成user-agent
class SpiderHide:
    def __init__(self):
        self.UA = 'Mozilla/5.0 (Windows NT 6.3; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/59.0.3071.109 Safari/537.36'
        self.IP = self.getmyip()
        
    def changeUA(self):
        self.UA = random.choice(User_Agent)
    
    def __repr__(self):
        print(self.UA)
        print(self.IP)
        return " "

    __str__ = __repr__
    
     
    
    def getmyip(self):
        req = urllib.request.Request('http://www.baidu.com/s?wd=ip')
        req.add_header('user-agent', self.UA)
        res = urllib.request.urlopen(req)
        page = res.read().decode('utf8')
        
        reg = re.compile(r'\s')
        page = reg.sub('', page)
        
        reg = re.compile(r'本机IP:(?:.*?);(.*?)</span>(.*?)</td>', re.M | re.S)
        ip = reg.findall(page)[0]
        return ip


